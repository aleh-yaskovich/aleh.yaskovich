public class Logic {
		
	public int sumTest1(int a, int b) {
		int sum;
		sum = a + b;
		return sum;
	}
	
	public int biggerNumberTest1(int a, int b) {
		int biggerNumber;
		if (a > b) {
			biggerNumber = a;
		} else {
			biggerNumber = b;
		}
		return biggerNumber;
	}	

}
